package week4labs;

public class IncrementDecrement_mon {

	public static void main(String[] args) 
	{
		int x = 5;
		int y = 99;
		
		++x;
		y++;
		
		System.out.println(x); //6
		System.out.println(y); //100
		
		System.out.println(++x); //7
		System.out.println(y++); //100
		int z = 10;
		System.out.println(++z * 2); //22 it gets declared as 10 first then incremented to 11 then multiplied
		
		
		

	}

}
